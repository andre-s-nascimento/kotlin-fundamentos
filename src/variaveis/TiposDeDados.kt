package variaveis

fun main() {
    //tipos de dados

    //numéricos
    // Byte = 8
    // Short = 16
    // Int = 32
    // Long = 64
    // Float = 32 -> 24.8
    // Double = 64 -> 53.11

    val byte: Byte = 8
    val short: Short = 360
    val int: Int = 1000
    val long: Long = 10000L
    val float: Float = 150.1f
    val double: Double = 110.1

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    //tipo Boolean
    // true / false
    val boolean: Boolean = true

    println(boolean)

    // Tipo textuais
    //
    // Char
    // String

    val char: Char = 'a'
    val string: String = "André S Nascimento"

    println(char)
    println(string)


}