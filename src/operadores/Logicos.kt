package operadores

fun main() {
    // Conjunção (E)
    println("---------Conjunção (E)---------")
    println(true && true)
    println(true && false)
    println(false && true)
    println(false && false)

    // Disjunção (OU)
    println("---------Disjunção OU---------")
    println(true || true)
    println(true || false)
    println(false || true)
    println(false || false)

    // Negação (NÃO)
    println("---------Negação (NÃO)---------")
    println(!true)
    println(!false)

}