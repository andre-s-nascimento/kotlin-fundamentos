package operadores

fun main() {
    val a = 5
    val b = 2

    // Soma
    println(a + b)
    // Subtração
    println(a - b)
    // Multiplicação
    println(a * b)
    // Divisão
    println(a / b.toDouble())

    // módulo
    println(a % b)

    // concatenação
    val c = "André " + "Nascimento"
    println(c)

}